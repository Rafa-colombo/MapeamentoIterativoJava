	private static final long serialVersionUID = 1L;

	//feed with magnitude 2.5+ Earthquakes
	private String earthquakesURL = "https://earthquake.usgs.gov/earthquakes/feed/v1.0/summary/2.5_week.atom";
	
	// The files containing city names and info and country names and info
	private String cityFile = "city-data.json";
	private String countryFile = "countries.geo.json";
	
	// The map
	private UnfoldingMap map;
	
	// Markers for each city
	private List<Marker> cityMarkers;
	// Markers for each earthquake
	private List<Marker> quakeMarkers;

	// A List of country markers
	private List<Marker> countryMarkers;
	
	private CommonMarker lastSelected;
	private CommonMarker lastClicked;
	
	public void setup() {		
		// (1) Initializing canvas and map tiles
		size(900, 700, OPENGL);
		
		AbstractMapProvider provider = new Microsoft.HybridProvider();
		map = new UnfoldingMap(this, 200, 50, 650, 600, provider);
	
		MapUtils.createDefaultEventDispatcher(this, map);
		
		// (2) Reading in earthquake data and geometric properties
	    //     STEP 1: load country features and markers
		List<Feature> countries = GeoJSONReader.loadData(this, countryFile);
		countryMarkers = MapUtils.createSimpleMarkers(countries);
		
		//     STEP 2: read in city data
		List<Feature> cities = GeoJSONReader.loadData(this, cityFile);
		cityMarkers = new ArrayList<Marker>();
		for(Feature city : cities) {
		  cityMarkers.add(new CityMarker(city));
		}
	    
		//     STEP 3: read in earthquake RSS feed
	    List<PointFeature> earthquakes = ParseFeed.parseEarthquake(this, earthquakesURL);
	    quakeMarkers = new ArrayList<Marker>();
	    
	    for(PointFeature feature : earthquakes) {
		  //check if LandQuake
		  if(isLand(feature)) {
		    quakeMarkers.add(new LandQuakeMarker(feature));
		  }
		  // OceanQuakes
		  else {
		    quakeMarkers.add(new OceanQuakeMarker(feature));
		  }
	    }

	    printQuakes();
	 		
	    // (3) Add markers to map
	    map.addMarkers(quakeMarkers);
	    map.addMarkers(cityMarkers);
	    
	    sortAndPrint(50);
	    
	}  // End setup
	
	
	public void draw() {
		background(0);
		map.draw();
		addKey();
		
	}
	
	
	// TODO: Add the method:
	private void sortAndPrint(int numToPrint) {
	    // 1. Criamos a lista de EarthquakeMarker
	    List<EarthquakeMarker> quakes = new ArrayList<EarthquakeMarker>();
	    for (Marker m : quakeMarkers) {
	        quakes.add((EarthquakeMarker)m);
	    }
	    
	    // 2. Ordenamos usando uma função anônima (Comparator)
	    // Isso resolve o erro do sort sem precisar de 'implements' na classe original
	    quakes.sort((q1, q2) -> {
	        return Float.compare(q2.getMagnitude(), q1.getMagnitude());
	    });
	    
	    // 3. Imprime os resultados
	    System.out.println("Printing top " + numToPrint + " earthquakes:");
	    int maxToPrint = Math.min(numToPrint, quakes.size());
	    
	    for (int i = 0; i < maxToPrint; i++) {
	        System.out.println(quakes.get(i).getTitle());
	    }
	}
	
	// Event handler that gets called automatically when the mouse moves.
	@Override
	public void mouseMoved()
	{
		// clear the last selection
		if (lastSelected != null) {
			lastSelected.setSelected(false);
			lastSelected = null;
		
		}
		selectMarkerIfHover(quakeMarkers);
		selectMarkerIfHover(cityMarkers);
		//loop();
	}
	
	// If there is a marker selected 
	private void selectMarkerIfHover(List<Marker> markers)
	{
		// Abort if there's already a marker selected
		if (lastSelected != null) {
			return;
		}
		
		for (Marker m : markers) 
		{
			CommonMarker marker = (CommonMarker)m;
			if (marker.isInside(map,  mouseX, mouseY)) {
				lastSelected = marker;
				marker.setSelected(true);
				return;
			}
		}
	}
	
	// The event handler for mouse clicks
	@Override
	public void mouseClicked()
	{
		if (lastClicked != null) {
			unhideMarkers();
			lastClicked = null;
		}
		else if (lastClicked == null) 
		{
			checkEarthquakesForClick();
			if (lastClicked == null) {
				checkCitiesForClick();
			}
		}
	}
	
	// Helper method that will check if a city marker was clicked on and respond appropriately
	private void checkCitiesForClick() {
	    if (lastClicked != null) return;

	    for (Marker marker : cityMarkers) {
	        if (!marker.isHidden() && marker.isInside(map, mouseX, mouseY)) {
	            lastClicked = (CommonMarker)marker;
	            
	            int quakeCount = 0;
	            float totalMag = 0;
	            float maxMag = 0;
	            EarthquakeMarker strongestQuake = null;

	            // Esconder outras cidades
	            for (Marker mhide : cityMarkers) {
	                if (mhide != lastClicked) mhide.setHidden(true);
	            }

	            // Filtrar terremotos e calcular estatísticas
	            for (Marker mhide : quakeMarkers) {
	                EarthquakeMarker quakeMarker = (EarthquakeMarker)mhide;
	                double distance = quakeMarker.getDistanceTo(marker.getLocation());
	                
	                if (distance > quakeMarker.threatCircle()) {
	                    quakeMarker.setHidden(true);
	                } else {
	                    // Estatísticas para a extensão
	                    quakeCount++;
	                    float mag = quakeMarker.getMagnitude();
	                    totalMag += mag;
	                    if (mag > maxMag) {
	                        maxMag = mag;
	                        strongestQuake = quakeMarker;
	                    }
	                }
	            }
	            
	            // EXIBIR EXTENSÃO NO CONSOLE
	            printCityStatistics(marker.getStringProperty("name"), quakeCount, totalMag, strongestQuake);
	            return;
	        }
	    }
	}
	
	// method to print city statistics
	private void printCityStatistics(String cityName, int count, float totalMag, EarthquakeMarker strongest) {
	    System.out.println("\n--- STATISTICS FOR " + cityName.toUpperCase() + " ---");
	    if (count > 0) {
	        System.out.println("Nearby Earthquakes: " + count);
	        System.out.println("Average Magnitude: " + (totalMag / count));
	        System.out.println("Strongest Threat: " + strongest.getTitle());
	    } else {
	        System.out.println("No nearby earthquakes detected in the threat zone.");
	    }
	    System.out.println("------------------------------------------\n");
	}
	
	// Helper method that will check if an earthquake marker was clicked on and respond appropriately
	private void checkEarthquakesForClick()
	{
		if (lastClicked != null) return;
		// Loop over the earthquake markers to see if one of them is selected
		for (Marker m : quakeMarkers) {
			EarthquakeMarker marker = (EarthquakeMarker)m;
			if (!marker.isHidden() && marker.isInside(map, mouseX, mouseY)) {
				lastClicked = marker;
				// Hide all the other earthquakes and hide
				for (Marker mhide : quakeMarkers) {
					if (mhide != lastClicked) {
						mhide.setHidden(true);
					}
				}
				for (Marker mhide : cityMarkers) {
					if (mhide.getDistanceTo(marker.getLocation()) 
							> marker.threatCircle()) {
						mhide.setHidden(true);
					}
				}
				return;
			}
		}
	}
	
	// loop over and unhide all markers
	private void unhideMarkers() {
		for(Marker marker : quakeMarkers) {
			marker.setHidden(false);
		}
			
		for(Marker marker : cityMarkers) {
			marker.setHidden(false);
		}
	}
	
